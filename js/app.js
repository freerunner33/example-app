var week = {0: 'Sunday', 1: 'Monday', 2: 'Tuesday', 3: 'Wednesday', 4: 'Thursday', 5: 'Friday', 6: 'Saturday'}

$(document).ready(function() {
	var day = (new Date()).getDay()

	getSchedule(day)

	$('h1').on('click', function() {
		day = (day + 1) % 7
		getSchedule(day)
	})
})

function getSchedule(day) {
	str = '<h1>' + week[day] + '</h1>'
	$.each(schedule[day], function(index, value) {
		str += '<h3>' + value.heading + '</h3>'
		$.each(value.elements, function(index, value) {
			str += '<li>' + value + '</li>'
		})
	})
	$('body').html(str)
}